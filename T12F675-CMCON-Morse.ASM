;	CMCON CIN+ > CIN- GPIO2 ON, CIN+ < CIN- OFF
		LIST		P=12F675
		INCLUDE		P12F675.INC
		CB		=	_CPD_OFF
		CB		&=	_CP_OFF
		CB		&=	_BODEN_ON
		CB		&=	_MCLRE_OFF
		CB		&=	_PWRTE_ON
		CB		&=	_WDT_OFF
		CB		&=	_INTRC_OSC_NOCLKOUT  ;_EXTRC_OSC_NOCLKOUT
		__CONFIG	CB
		ORG			0				;
		GOTO		INIT			;
INIT
		BSF			STATUS,RP0		;BANK1
		CALL		H'3FF'			;OSCCAL
		MOVWF		OSCCAL			;OSCCAL
		MOVLW		B'00000011'		;Input Port GPIO,3 AN3
		MOVWF		TRISIO			;
		MOVLW		B'01011011'		;
		MOVWF		ANSEL			;
		BCF			STATUS,RP0		;
		MOVLW		B'00000001'		;CM2:CM0 = 001
		MOVWF		CMCON			;
		CLRF		GPIO			;

MAIN
		NOP
		GOTO		MAIN			;
		END					 		;